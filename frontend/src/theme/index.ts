export const applyTheme = (mode: ThemeMode) => {
  const theme = themes[mode];
  const root = document.documentElement;
  Object.entries(theme).forEach(([key, value]) => {
    root.style.setProperty(key, value);
  });
  root.setAttribute('data-theme', mode);
};
